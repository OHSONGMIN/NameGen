package com.spring.namegen.dto;

public class CarDTO {

    private String car;
    private String keyword;

    public CarDTO() {
    }

    public String getCar() {
        return car;
    }

    public void setCar(String car) {
        this.car = car;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

}
